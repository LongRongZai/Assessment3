create table Country (
CountryName varchar(50) primary key,
CountryConfirmed int(20) not null,
CountryRecovered int(20) not null,
CountryDeaths int(20) not null,
CountryPopulation int(20) not null,
CountryContinent varchar(20) not null,
CountryArea int(20) not null,
CountryCapitalCity varchar(20) 
);
create table Region (
RegionName varchar(50) primary key,
RegionConfirmed int(20) not null,
RegionRecovered int(20) not null,
RegionDeaths int(20) not null
);
create table CountryRegion (
CountryName varchar(50) ,
RegionName varchar(50) ,
foreign key(CountryName) references Country(CountryName),
foreign key(RegionName) references Region(RegionName),
primary key(CountryName,RegionName)
);
